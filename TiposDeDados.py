# Exemplo 1: Inteiro
x = 10
print(type(x))  # Saída: <class 'int'>
print(x)

# Exemplo 2: Float
y = 3.14
print(type(y))  # Saída: <class 'float'>
print(y)

# Exemplo 2.1: Float
y1 = .7
print(type(y))  # Saída: <class 'float'>
print(y1)

# Exemplo 3: String
z = "Olá, mundo!"
print(type(z))  # Saída: <class 'str'>
print(z)

# Exemplo 4: Lista
lista = [1, 2, 3]
print(type(lista))  # Saída: <class 'list'>
print(lista)

# Exemplo 5: Dicionário
dicionario = {"chave": "valor"}
print(type(dicionario))  # Saída: <class 'dict'>
print(dicionario)

# Exemplo 6: Exponeciação
exp = 2. ** 3
print(exp)
print(type(exp)) # Saída: <class 'Float'>

# Exemplo 6.1: Exponeciação
esp1 = 2e-3 # 2 * 10 ** -3
print(esp1) # Saída: <class 'Float'>
