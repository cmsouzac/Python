### Operações Matematica

#   Adição: a + b
#   Subtração: a - b
#   Multiplicaçao: a * b
#   Divisão: a / b
# #

# Atribuindo valores às variáveis
a = 5
b = 3

# Imprimindo a soma diretamente
print(a + b)

print(a - b)

print(a * b)

print(a / b)

# Imprimindo os valores de 'a' e 'b' individualmente
print(a, b)

### Operações Avançadas

#   Divisão Inteira: a // b 
#   Modulo (Resto da Divisão): a % b
#   Exponenciação: a ** b
#   Operações de Comparação: ==, !=, <, <=, >, >=
# #

print(a // b)
print(a % b) 
print(a ** b)

### Operações Por comparação
print(a == b)
print(a != b)
print(a < b)
print(a > b)
print(a <= b)
print(a >= b)



